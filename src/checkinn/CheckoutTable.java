package checkinn;

/**
 *
 * @author Rodoshi
 */
public class CheckoutTable {

    String name, mail, id, money, time;

    public CheckoutTable(String name, String mail, String id, String money, String time) {
        this.name = name;
        this.mail = mail;
        this.id = id;
        this.money = money;
        this.time = time;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getMail() {
        return mail;
    }

    public void setMail(String mail) {
        this.mail = mail;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getMoney() {
        return money;
    }

    public void setMoney(String money) {
        this.money = money;
    }

    public String getTime() {
        return time;
    }

    public void setTime(String time) {
        this.time = time;
    }

}
